  <script>
      window.addEventListener("load", () => {
        const loopContainer = document.querySelector(".loop-container");
        const wrapper = document.querySelector(".testimonial-wrapper");
        const scroll = document.querySelector(".testimonial-scroll");

        // Clone the original content to create an infinite scroll effect
        const clone = scroll.cloneNode(true);
        wrapper.appendChild(clone);

        let offset = 0;
        const speed = 0.4; // scroll speed
        const originalHeight = scroll.getBoundingClientRect().height;
        let paused = false;

        // Pause on hover
        loopContainer.addEventListener("mouseenter", () => paused = true);
        loopContainer.addEventListener("mouseleave", () => paused = false);

        function animate() {
          if (!paused) {
            offset += speed;
            // Smoothly reset when the original height is exceeded
            if (offset >= originalHeight) {
              offset -= originalHeight;
            }
            wrapper.style.transform = `translateY(-${offset}px)`;
          }
          requestAnimationFrame(animate);
        }

        wrapper.style.willChange = "transform";
        animate();
      });
    </script>

